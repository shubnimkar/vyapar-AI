const CHART_COLORS = ['#3b82f6', '#10b981', '#f59e0b', '#8b5cf6', '#ef4444', '#06b6d4', '#f97316'];

function clean(text: string): string {
  return text.replace(/[*_`#>]/g, '').replace(/\s+/g, ' ').trim();
}

function escapeHtml(text: string): string {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

function truncate(text: string, max: number): string {
  const normalized = clean(text);
  if (normalized.length <= max) return normalized;
  const cut = normalized.slice(0, max);
  const wordBreak = cut.lastIndexOf(' ');
  return `${cut.slice(0, wordBreak > 0 ? wordBreak : max).trim()}...`;
}

function severityStyle(severity: SeverityLevel | undefined) {
  switch (severity) {
    case 'critical':
      return { bg: '#fef2f2', border: '#fecaca', text: '#dc2626' };
    case 'warning':
      return { bg: '#fffbeb', border: '#fde68a', text: '#d97706' };
    case 'good':
      return { bg: '#f0fdf4', border: '#bbf7d0', text: '#16a34a' };
    default:
      return { bg: '#eff6ff', border: '#bfdbfe', text: '#2563eb' };
  }
}

function renderLineChart(title: string, labels: string[], values: number[]): string {
  if (!values.length) return '';
  const width = 360;
  const height = 210;
  const chartLeft = 36;
  const chartTop = 18;
  const chartWidth = 300;
  const chartHeight = 130;
  const max = Math.max(...values);
  const min = Math.min(...values);
  const range = max - min || 1;
  const points = values
    .map((value, index) => {
      const x = chartLeft + (index / Math.max(values.length - 1, 1)) * chartWidth;
      const y = chartTop + chartHeight - ((value - min) / range) * chartHeight;
      return `${x},${y}`;
    })
    .join(' ');

  const xLabels = labels.slice(-5).map((label, index, arr) => {
    const x = chartLeft + (index / Math.max(arr.length - 1, 1)) * chartWidth;
    return `<text x="${x}" y="${chartTop + chartHeight + 18}" font-size="9" fill="#64748b" text-anchor="middle">${escapeHtml(label.slice(5))}</text>`;
  });

  return `
    <div class="chart-card">
      <div class="chart-title">${escapeHtml(title)}</div>
      <svg viewBox="0 0 ${width} ${height}" class="chart-svg">
        <line x1="${chartLeft}" y1="${chartTop + chartHeight}" x2="${chartLeft + chartWidth}" y2="${chartTop + chartHeight}" stroke="#cbd5e1" stroke-width="1"/>
        <line x1="${chartLeft}" y1="${chartTop}" x2="${chartLeft}" y2="${chartTop + chartHeight}" stroke="#cbd5e1" stroke-width="1"/>
        <polyline fill="none" stroke="#3b82f6" stroke-width="3" points="${points}" />
        ${points
          .split(' ')
          .map((point) => {
            const [x, y] = point.split(',');
            return `<circle cx="${x}" cy="${y}" r="3.5" fill="#ffffff" stroke="#3b82f6" stroke-width="2"/>`;
          })
          .join('')}
        ${xLabels.join('')}
      </svg>
    </div>`;
}

function renderVerticalBars(title: string, labels: string[], values: number[], color: string): string {
  if (!values.length) return '';
  const width = 360;
  const height = 210;
  const chartLeft = 28;
  const chartTop = 18;
  const chartWidth = 316;
  const chartHeight = 122;
  const max = Math.max(...values) || 1;
  const barWidth = Math.max(18, Math.floor(chartWidth / Math.max(values.length, 1)) - 10);

  const bars = values.slice(0, 6).map((value, index) => {
    const x = chartLeft + index * (barWidth + 12);
    const barHeight = (value / max) * chartHeight;
    const y = chartTop + chartHeight - barHeight;
    const label = truncate(labels[index] || '', 14);
    return `
      <rect x="${x}" y="${y}" width="${barWidth}" height="${barHeight}" rx="4" fill="${color}" />
      <text x="${x + barWidth / 2}" y="${chartTop + chartHeight + 14}" font-size="8.5" fill="#64748b" text-anchor="middle">${escapeHtml(label)}</text>
    `;
  });

  return `
    <div class="chart-card">
      <div class="chart-title">${escapeHtml(title)}</div>
      <svg viewBox="0 0 ${width} ${height}" class="chart-svg">
        <line x1="${chartLeft}" y1="${chartTop + chartHeight}" x2="${chartLeft + chartWidth}" y2="${chartTop + chartHeight}" stroke="#cbd5e1" stroke-width="1"/>
        ${bars.join('')}
      </svg>
    </div>`;
}

function renderHorizontalBars(title: string, labels: string[], values: number[], color: string): string {
  if (!values.length) return '';
  const width = 360;
  const height = 210;
  const chartLeft = 130;
  const chartTop = 18;
  const chartWidth = 190;
  const rowHeight = 24;
  const max = Math.max(...values) || 1;

  const bars = values.slice(0, 5).map((value, index) => {
    const y = chartTop + index * rowHeight;
    const barWidth = (value / max) * chartWidth;
    return `
      <text x="${chartLeft - 8}" y="${y + 14}" font-size="9" fill="#475569" text-anchor="end">${escapeHtml(truncate(labels[index] || '', 20))}</text>
      <rect x="${chartLeft}" y="${y + 3}" width="${barWidth}" height="14" rx="4" fill="${color}" />
    `;
  });

  return `
    <div class="chart-card">
      <div class="chart-title">${escapeHtml(title)}</div>
      <svg viewBox="0 0 ${width} ${height}" class="chart-svg">
        ${bars.join('')}
      </svg>
    </div>`;
}

function renderDonutChart(title: string, labels: string[], values: number[]): string {
  if (!values.length) return '';
  const total = values.reduce((sum, value) => sum + value, 0) || 1;
  const centerX = 110;
  const centerY = 90;
  const radius = 52;
  const innerRadius = 28;

  let cumulative = 0;
  const segments = values.slice(0, 5).map((value, index) => {
    const startAngle = (cumulative / total) * Math.PI * 2 - Math.PI / 2;
    cumulative += value;
    const endAngle = (cumulative / total) * Math.PI * 2 - Math.PI / 2;
    const x1 = centerX + radius * Math.cos(startAngle);
    const y1 = centerY + radius * Math.sin(startAngle);
    const x2 = centerX + radius * Math.cos(endAngle);
    const y2 = centerY + radius * Math.sin(endAngle);
    const largeArc = endAngle - startAngle > Math.PI ? 1 : 0;
    return `<path d="M ${centerX} ${centerY} L ${x1} ${y1} A ${radius} ${radius} 0 ${largeArc} 1 ${x2} ${y2} Z" fill="${CHART_COLORS[index % CHART_COLORS.length]}" />`;
  });

  const legend = labels.slice(0, 5).map((label, index) => {
    const y = 34 + index * 20;
    return `
      <rect x="210" y="${y - 8}" width="10" height="10" rx="2" fill="${CHART_COLORS[index % CHART_COLORS.length]}" />
      <text x="226" y="${y}" font-size="9" fill="#475569">${escapeHtml(truncate(label, 18))}</text>
    `;
  });

  return `
    <div class="chart-card">
      <div class="chart-title">${escapeHtml(title)}</div>
      <svg viewBox="0 0 360 210" class="chart-svg">
        ${segments.join('')}
        <circle cx="${centerX}" cy="${centerY}" r="${innerRadius}" fill="#ffffff" />
        <text x="${centerX}" y="${centerY - 2}" text-anchor="middle" font-size="10" font-weight="700" fill="#0f172a">Breakdown</text>
        <text x="${centerX}" y="${centerY + 12}" text-anchor="middle" font-size="8.5" fill="#64748b">${values.length} items</text>
        ${legend.join('')}
      </svg>
    </div>`;
}

function renderBenchmark(benchmark: BenchmarkData | null | undefined, labels: Labels): string {
  if (!benchmark) return '';
  const max = Math.max(benchmark.topPerformers, benchmark.industryAverage, benchmark.yourMetric, 1);
  const yourWidth = (benchmark.yourMetric / max) * 100;
  const avgWidth = (benchmark.industryAverage / max) * 100;
  const topWidth = (benchmark.topPerformers / max) * 100;

  return `
    <div class="section-block">
      <div class="section-title">${escapeHtml(labels.benchmark)}</div>
      <div class="benchmark-card">
        <div class="benchmark-status">${escapeHtml(benchmark.metricName)}</div>
        <div class="benchmark-row">
          <span>${escapeHtml(labels.benchmarkLabels.yourBusiness)}</span>
          <strong>${benchmark.yourMetric}${escapeHtml(benchmark.unit)}</strong>
        </div>
        <div class="benchmark-bar"><span style="width:${yourWidth}%"></span></div>
        <div class="benchmark-row">
          <span>${escapeHtml(labels.benchmarkLabels.industryAverage)}</span>
          <strong>${benchmark.industryAverage}${escapeHtml(benchmark.unit)}</strong>
        </div>
        <div class="benchmark-bar secondary"><span style="width:${avgWidth}%"></span></div>
        <div class="benchmark-row">
          <span>${escapeHtml(labels.benchmarkLabels.topPerformers)}</span>
          <strong>${benchmark.topPerformers}${escapeHtml(benchmark.unit)}</strong>
        </div>
        <div class="benchmark-bar success"><span style="width:${topWidth}%"></span></div>
      </div>
    </div>`;
}

