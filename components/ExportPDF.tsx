export default function ExportPDF({ insights, language, benchmark }: ExportPDFProps) {
  const handleExport = async () => {
    const [{ default: jsPDF }, { default: html2canvas }] = await Promise.all([
      import('jspdf'),
      import('html2canvas'),
    ]);

    const html = buildReportHTML(insights, language, benchmark);
    const iframe = document.createElement('iframe');
    iframe.style.cssText =
      'position:fixed;top:-9999px;left:-9999px;width:794px;height:1123px;border:none;background:white;';
    document.body.appendChild(iframe);

    const iframeDoc = iframe.contentDocument || iframe.contentWindow?.document;
    if (!iframeDoc) {
      document.body.removeChild(iframe);
      return;
    }

    iframeDoc.open();
    iframeDoc.write(html);
    iframeDoc.close();

    await new Promise((resolve) => setTimeout(resolve, 400));

    const pdf = new jsPDF({ unit: 'mm', format: 'a4', orientation: 'portrait' });
    const pdfWidth = pdf.internal.pageSize.getWidth();
    const pageNodes = Array.from(iframeDoc.querySelectorAll('.page'));

    for (let index = 0; index < pageNodes.length; index += 1) {
      const pageNode = pageNodes[index] as HTMLElement;
      const canvas = await html2canvas(pageNode, {
        scale: 2,
        useCORS: true,
        backgroundColor: '#ffffff',
        width: 794,
        windowWidth: 794,
      });

      if (index > 0) {
        pdf.addPage();
      }

      const imageData = canvas.toDataURL('image/png');
      const imageHeight = (canvas.height * pdfWidth) / canvas.width;
      pdf.addImage(imageData, 'PNG', 0, 0, pdfWidth, imageHeight);
    }

    document.body.removeChild(iframe);

    const filename =
      language === 'hi'
        ? 'vyapar-business-report-hi.pdf'
        : language === 'mr'
        ? 'vyapar-business-report-mr.pdf'
        : 'vyapar-business-report.pdf';

    pdf.save(filename);
  };

  return (
    <Button
      onClick={handleExport}
      variant="secondary"
      fullWidth
      className="border-primary-200 text-primary-700 hover:bg-primary-50"
    >
      <span className="text-2xl">📥</span>
      <span>{LABELS[language].buttonText}</span>
    </Button>
  );
}
